import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const imageMode = process.env.BUNDLE_IMAGE_MODE === 'external' ? 'external' : 'inline';

const sopJsonPath = resolve(projectRoot, 'src/assets/content/sop.json');
const glossaryJsonPath = resolve(projectRoot, 'src/assets/content/glossary.json');
const workspaceConfigPresetsJsonPath = resolve(
  projectRoot,
  'src/assets/content/workspace-config-presets.json',
);

const sopOutputPath = resolve(projectRoot, 'src/app/core/data/sop.data.ts');
const glossaryOutputPath = resolve(projectRoot, 'src/app/core/data/glossary.data.ts');
const workspaceConfigPresetsOutputPath = resolve(
  projectRoot,
  'src/app/core/data/workspace-config-presets.data.ts',
);

function toTsModule(constantName, data, sourcePath) {
  const json = JSON.stringify(data, null, 2);
  return `// AUTO-GENERATED FILE. DO NOT EDIT.\n// Source: ${sourcePath}\n// Generated by: npm run bundle-data\n\nexport const ${constantName}: unknown = ${json};\n`;
}

async function readJson(path) {
  const raw = await readFile(path, 'utf8');
  return JSON.parse(raw);
}

async function writeGenerated(path, content) {
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, content, 'utf8');
}

/** @param {string} contentString @param {{ id: string }[]} glossaryTerms */
function parseContent(contentString, glossaryTerms) {
  if (!contentString) {
    return [];
  }

  const segments = [];
  const re = /\[\[img:([^|\]]+)(?:\|([^\]]+))?\]\]|\[\[([^\]]+)\]\]/g;

  let lastIndex = 0;
  let match;

  while ((match = re.exec(contentString)) !== null) {
    if (match.index > lastIndex) {
      segments.push({
        type: 'text',
        value: contentString.slice(lastIndex, match.index),
      });
    }

    if (match[1]) {
      const imageFilename = match[1].trim();
      const imagePath = resolve(projectRoot, `src/assets/images/${imageFilename}`);

      const imageSegment = {
        type: 'image',
        src: `assets/images/${imageFilename}`,
        alt: match[2] ? match[2].trim() : 'SOP Image',
      };

      if (imageMode === 'inline') {
        try {
          const imageBuffer = readFileSync(imagePath);
          imageSegment.base64Data = imageBuffer.toString('base64');
        } catch {
          console.warn(`⚠️ Could not read image for base64 encoding: ${imagePath}`);
        }
      }

      segments.push(imageSegment);
    } else if (match[3]) {
      const termId = match[3].trim().toLowerCase();
      const termExists = glossaryTerms.some((t) => t.id === termId);

      if (!termExists) {
        console.warn(`⚠️ Warning: Glossary term '[[${termId}]]' not found.`);
      }

      segments.push({
        type: 'term',
        termId,
        display: match[3].trim(),
      });
    }

    lastIndex = re.lastIndex;
  }

  if (lastIndex < contentString.length) {
    segments.push({
      type: 'text',
      value: contentString.slice(lastIndex),
    });
  }

  return segments;
}

function toAssetsImagePath(imageSrc) {
  if (typeof imageSrc !== 'string') {
    return null;
  }
  const cleaned = imageSrc.trim().replace(/^\.?\//, '');
  if (!cleaned.startsWith('assets/images/')) {
    return null;
  }
  return resolve(projectRoot, 'src', cleaned);
}

function readImageBase64(imageSrc) {
  if (imageMode !== 'inline') {
    return undefined;
  }
  const imagePath = toAssetsImagePath(imageSrc);
  if (!imagePath) {
    return undefined;
  }
  try {
    const imageBuffer = readFileSync(imagePath);
    return imageBuffer.toString('base64');
  } catch {
    console.warn(`⚠️ Could not read image for base64 encoding: ${imagePath}`);
    return undefined;
  }
}

/** @param {unknown} content */
function normalizeContentSegments(content) {
  if (!Array.isArray(content)) {
    return content;
  }

  return content.map((segment) => {
    if (!segment || typeof segment !== 'object') {
      return segment;
    }

    const s = /** @type {Record<string, unknown>} */ (segment);
    if (s.type !== 'image') {
      return segment;
    }

    if (imageMode === 'external') {
      // External mode never ships inlined base64 payloads.
      const imageWithoutBase64 = { ...s };
      delete imageWithoutBase64.base64Data;
      return imageWithoutBase64;
    }

    const base64Data =
      typeof s.base64Data === 'string' && s.base64Data.length > 0
        ? s.base64Data
        : readImageBase64(s.src);
    return base64Data ? { ...s, base64Data } : s;
  });
}

/** @param {unknown} modules @param {{ id: string }[]} glossaryTerms */
function normalizeSopModules(modules, glossaryTerms) {
  if (!Array.isArray(modules)) {
    return modules;
  }

  return modules.map((node) => {
    if (!node || typeof node !== 'object') {
      return node;
    }
    const n = /** @type {Record<string, unknown>} */ (node);
    const content =
      typeof n.content === 'string'
        ? parseContent(n.content, glossaryTerms)
        : normalizeContentSegments(n.content);
    const children = normalizeSopModules(n.children, glossaryTerms);

    return { ...n, content, children };
  });
}

async function main() {
  const [sopData, glossaryData, workspaceConfigPresets] = await Promise.all([
    readJson(sopJsonPath),
    readJson(glossaryJsonPath),
    readJson(workspaceConfigPresetsJsonPath),
  ]);

  const glossaryTerms = Array.isArray(glossaryData) ? glossaryData : [];
  const normalizedSop = normalizeSopModules(sopData, glossaryTerms);

  await Promise.all([
    writeGenerated(
      sopOutputPath,
      toTsModule('SOP_DATA', normalizedSop, 'src/assets/content/sop.json'),
    ),
    writeGenerated(
      glossaryOutputPath,
      toTsModule('GLOSSARY_DATA', glossaryData, 'src/assets/content/glossary.json'),
    ),
    writeGenerated(
      workspaceConfigPresetsOutputPath,
      toTsModule(
        'WORKSPACE_CONFIG_PRESETS_DATA',
        workspaceConfigPresets,
        'src/assets/content/workspace-config-presets.json',
      ),
    ),
  ]);

  console.log(
    `Bundled SOP, glossary, and workspace config presets into TypeScript constants (BUNDLE_IMAGE_MODE=${imageMode}).`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
